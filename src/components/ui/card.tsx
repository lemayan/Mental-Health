import * as React from 'react';
import { cn } from '@/lib/utils';

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'elevated' | 'outlined' | 'interactive';
  padding?: 'none' | 'sm' | 'md' | 'lg';
}

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', padding = 'md', children, ...props }, ref) => {
    return (
      <div
        ref={ref}
        className={cn(
          'rounded-xl bg-white',
          // Variant styles
          {
            'border border-neutral-200 shadow-md': variant === 'default',
            'shadow-lg': variant === 'elevated',
            'border border-neutral-200': variant === 'outlined',
            'border border-neutral-200 shadow-md transition-all duration-200 hover:shadow-lg hover:-translate-y-0.5 cursor-pointer':
              variant === 'interactive',
          },
          // Padding styles
          {
            'p-0': padding === 'none',
            'p-4': padding === 'sm',
            'p-6': padding === 'md',
            'p-8': padding === 'lg',
          },
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

